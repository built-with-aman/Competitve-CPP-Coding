// The C++ STL (Standard Template Library) is a powerful set of template-based classes and functions that 
// provide common data structures and algorithmsâ€”ready to use, efficient, and type-safe.

// main componenets of the STL include:
// 1. Containers: Data structures that store collections of objects.
//    - Sequence Containers: `vector`, `list`, `deque`
//    - Associative Containers: `set`, `map`, `multiset`, `multimap`
//    - Unordered Associative Containers: `unordered_set`, `unordered_map`, `unordered_multiset`, `unordered_multimap`
//    - Container Adapters: `stack`, `queue`, `priority_queue`

// 2. Algorithms: Functions that operate on containers, such as sorting, searching, and manipulating data.
//    - Examples include `sort`, `find`, `accumulate`, `for_each`, etc.

// 3. Iterators: Objects that allow traversal of containers.
//    - Types include `input_iterator`, `output_iterator`, `forward_iterator`, `bidirectional_iterator`, and `random_access_iterator`.
//    - Iterators can be used with algorithms to apply operations on container elements.

// 4. Function Objects (Functors): Objects that can be called as if they are functions.
//    - STL provides predefined functors like `plus`, `minus`, `multiplies`, etc.
//    - You can also create your own functors by defining a class with an `operator()` method.

// 5. Allocators: Classes that handle memory allocation for containers.
//    - STL uses allocators to abstract memory management, allowing customization of memory allocation strategies.


